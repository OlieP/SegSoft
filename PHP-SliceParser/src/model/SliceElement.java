package model;

public class SliceElement {
	private String _kind;
	private String _name;

	public SliceElement() {
	}
	
	public String getKind() {
		return _kind;
	}
	public void setKind(String kind) {
		this._kind = kind;
	}
	public String getName() {
		return _name;
	}
	public void setName(String name) {
		this._name = name;
	}

}
